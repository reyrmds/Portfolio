package erp.hrms.beans;

public class PayGrade {

private int payGradeId;
private String payGradeName;
private float payGradeMinimum;
private float payGradeMaximum;
public int getPayGradeId() {
	return payGradeId;
}
public void setPayGradeId(int payGradeId) {
	this.payGradeId = payGradeId;
}
public String getPayGradeName() {
	return payGradeName;
}
public void setPayGradeName(String payGradeName) {
	this.payGradeName = payGradeName;
}
public float getPayGradeMinimum() {
	return payGradeMinimum;
}
public void setPayGradeMinimum(float payGradeMinimum) {
	this.payGradeMinimum = payGradeMinimum;
}
public float getPayGradeMaximum() {
	return payGradeMaximum;
}
public void setPayGradeMaximum(float payGradeMaximum) {
	this.payGradeMaximum = payGradeMaximum;
}
}


